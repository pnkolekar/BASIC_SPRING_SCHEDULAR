    private final RestTemplate restTemplate;

    private static final String PDF_DIRECTORY =
            "/data/PRO_DATA/WORKORDERS/";

    public void downloadAllPdfs() {

        List<String> consumerIds = getConsumerIds();

        ExecutorService executor =
                Executors.newFixedThreadPool(20);

        for (String consumerId : consumerIds) {

            executor.submit(() -> {
                try {
                    downloadPdf(consumerId);
                } catch (Exception e) {
                    log.error("Failed for consumer {}",
                            consumerId, e);
                }
            });
        }

        executor.shutdown();
    }

    private void downloadPdf(String consumerId) throws IOException {

        String apiUrl =
                "https://offgridmtsup.mahadiscom.in/MTSKPSolarEnergy/pdfReport?reportName=mtskpWONEW&circle_code=" + consumerId;

        ResponseEntity<Resource> response =
                restTemplate.exchange(
                        apiUrl,
                        HttpMethod.GET,
                        null,
                        Resource.class);

        if (response.getStatusCode().is2xxSuccessful()
                && response.getBody() != null) {

            Path path = Paths.get(
                    PDF_DIRECTORY,
                    consumerId + ".pdf");

            Files.createDirectories(path.getParent());

            try (InputStream in =
                         response.getBody().getInputStream()) {

                Files.copy(
                        in,
                        path,
                        StandardCopyOption.REPLACE_EXISTING);
            }

            log.info("PDF downloaded for {}", consumerId);
        }
    }

